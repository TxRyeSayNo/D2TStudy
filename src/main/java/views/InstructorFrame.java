    private void editCourse() {
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn kh√≥a h·ªçc!");
            return;
        }
        
        int id = (int) tableModel.getValueAt(row, 0);
        Course course = courseDAO.findById(id);
        
        JDialog dialog = new JDialog(this, "S·ª≠a kh√≥a h·ªçc", true);
        dialog.setSize(450, 300);
        dialog.setLocationRelativeTo(this);
        
        JPanel panel = new JPanel(new GridLayout(5, 2, 5, 5));
        panel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        
        JTextField txtTitle = new JTextField(course.getTitle());
        JTextArea txtDescription = new JTextArea(course.getDescription(), 3, 20);
        txtDescription.setLineWrap(true);
        JTextField txtPrice = new JTextField(String.valueOf(course.getPrice()));
        
        panel.add(new JLabel("T√™n kh√≥a h·ªçc:"));
        panel.add(txtTitle);
        panel.add(new JLabel("M√¥ t·∫£:"));
        panel.add(new JScrollPane(txtDescription));
        panel.add(new JLabel("Gi√° (VNƒê):"));
        panel.add(txtPrice);
        
        JButton btnSave = new JButton("L∆∞u");
        JButton btnCancel = new JButton("H·ªßy");
        
        btnSave.addActionListener(e -> {
            try {
                course.setTitle(txtTitle.getText());
                course.setDescription(txtDescription.getText());
                course.setPrice(Double.parseDouble(txtPrice.getText()));
                
                if (courseDAO.update(course)) {
                    JOptionPane.showMessageDialog(dialog, "C·∫≠p nh·∫≠t th√†nh c√¥ng!");
                    loadData();
                    dialog.dispose();
                } else {
                    JOptionPane.showMessageDialog(dialog, "C·∫≠p nh·∫≠t th·∫•t b·∫°i!");
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(dialog, "Gi√° kh√¥ng h·ª£p l·ªá!");
            }
        });
        
        btnCancel.addActionListener(e -> dialog.dispose());
        
        panel.add(btnSave);
        panel.add(btnCancel);
        
        dialog.add(panel);
        dialog.setVisible(true);
    }
    
    private void deleteCourse() {
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn kh√≥a h·ªçc!");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "X√°c nh·∫≠n x√≥a kh√≥a h·ªçc n√†y?");
        if (confirm == JOptionPane.YES_OPTION) {
            int id = (int) tableModel.getValueAt(row, 0);
            if (courseDAO.delete(id)) {
                JOptionPane.showMessageDialog(this, "X√≥a th√†nh c√¥ng!");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "X√≥a th·∫•t b·∫°i!");
            }
        }
    }
    
