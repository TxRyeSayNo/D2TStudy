    private void loadData() {
        tableModel.setRowCount(0);
        List<User> users = userDAO.findAll();
        for (User user : users) {
            tableModel.addRow(new Object[]{
                user.getId(),
                user.getUsername(),
                user.getFullname(),
                user.getEmail(),
                user.getRole()
            });
        }
    }
    
