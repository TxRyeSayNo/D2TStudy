    private void editUser() {
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn user!");
            return;
        }
        
        int id = (int) tableModel.getValueAt(row, 0);
        User user = userDAO.findById(id);
        
        JDialog dialog = new JDialog(this, "S·ª≠a User", true);
        dialog.setSize(400, 300);
        dialog.setLocationRelativeTo(this);
        
        JPanel panel = new JPanel(new GridLayout(6, 2, 5, 5));
        panel.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        
        JTextField txtUsername = new JTextField(user.getUsername());
        JTextField txtPassword = new JTextField(user.getPassword());
        JTextField txtFullname = new JTextField(user.getFullname());
        JTextField txtEmail = new JTextField(user.getEmail());
        JComboBox<String> cboRole = new JComboBox<>(new String[]{"ADMIN", "INSTRUCTOR", "STUDENT"});
        cboRole.setSelectedItem(user.getRole());
        
        panel.add(new JLabel("Username:"));
        panel.add(txtUsername);
        panel.add(new JLabel("Password:"));
        panel.add(txtPassword);
        panel.add(new JLabel("Fullname:"));
        panel.add(txtFullname);
        panel.add(new JLabel("Email:"));
        panel.add(txtEmail);
        panel.add(new JLabel("Role:"));
        panel.add(cboRole);
        
        JButton btnSave = new JButton("L∆∞u");
        JButton btnCancel = new JButton("H·ªßy");
        
        btnSave.addActionListener(e -> {
            user.setUsername(txtUsername.getText());
            user.setPassword(txtPassword.getText());
            user.setFullname(txtFullname.getText());
            user.setEmail(txtEmail.getText());
            user.setRole(cboRole.getSelectedItem().toString());
            
            if (userDAO.update(user)) {
                JOptionPane.showMessageDialog(dialog, "C·∫≠p nh·∫≠t th√†nh c√¥ng!");
                loadData();
                dialog.dispose();
            } else {
                JOptionPane.showMessageDialog(dialog, "C·∫≠p nh·∫≠t th·∫•t b·∫°i!");
            }
        });
        
        btnCancel.addActionListener(e -> dialog.dispose());
        
        panel.add(btnSave);
        panel.add(btnCancel);
        
        dialog.add(panel);
        dialog.setVisible(true);
    }
    
    private void deleteUser() {
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn user!");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "X√°c nh·∫≠n x√≥a?");
        if (confirm == JOptionPane.YES_OPTION) {
            int id = (int) tableModel.getValueAt(row, 0);
            if (userDAO.delete(id)) {
                JOptionPane.showMessageDialog(this, "X√≥a th√†nh c√¥ng!");
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "X√≥a th·∫•t b·∫°i!");
            }
        }
    }
    
