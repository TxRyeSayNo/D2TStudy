    private void loadData() {
        loadAllCourses();
        loadMyEnrollments();
    }
    
    private void loadAllCourses() {
        modelCourses.setRowCount(0);
        List<Course> courses = courseDAO.findAll();
        
        for (Course course : courses) {
            modelCourses.addRow(new Object[]{
                course.getId(),
                course.getTitle(),
                course.getDescription(),
                course.getPriceText(),
                course.getInstructorName()
            });
        }
    }
    
    private void loadMyEnrollments() {
        modelEnrollments.setRowCount(0);
        int studentId = Session.getCurrentUser().getId();
        List<Enrollment> enrollments = enrollmentDAO.findByStudent(studentId);
        
        for (Enrollment enrollment : enrollments) {
            modelEnrollments.addRow(new Object[]{
                enrollment.getId(),
                enrollment.getCourseTitle(),
                enrollment.getEnrolledDate()
            });
        }
    }
    
    private void enrollCourse() {
        int row = tableCourses.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn kh√≥a h·ªçc!");
            return;
        }
        
        int courseId = (int) modelCourses.getValueAt(row, 0);
        int studentId = Session.getCurrentUser().getId();
        
        // Check if already enrolled
        if (enrollmentDAO.isEnrolled(studentId, courseId)) {
            JOptionPane.showMessageDialog(this, "B·∫°n ƒë√£ ƒëƒÉng k√Ω kh√≥a h·ªçc n√†y r·ªìi!");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "X√°c nh·∫≠n ƒëƒÉng k√Ω kh√≥a h·ªçc n√†y?", 
            "X√°c nh·∫≠n", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            Enrollment enrollment = new Enrollment(studentId, courseId);
            
            if (enrollmentDAO.insert(enrollment)) {
                JOptionPane.showMessageDialog(this, 
                    "ƒêƒÉng k√Ω th√†nh c√¥ng!\nB·∫°n c√≥ th·ªÉ b·∫Øt ƒë·∫ßu h·ªçc ngay.", 
                    "Th√†nh c√¥ng", 
                    JOptionPane.INFORMATION_MESSAGE);
                loadMyEnrollments();
            } else {
                JOptionPane.showMessageDialog(this, "ƒêƒÉng k√Ω th·∫•t b·∫°i!");
            }
        }
    }
    
